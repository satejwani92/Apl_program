# length (len) method
s = {1,8,2,3}
print(len(s))

#remove method
s.remove(8) # remove 8 from set
s.remove(1) # remove 1 from set
print(s)

#pop method
s.pop() #remove random number feom set
print(s)

#clear method
s.clear()
print(s)


